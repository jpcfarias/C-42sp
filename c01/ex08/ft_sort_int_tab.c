void	ft_sort_int_tab(int	*tab, int size)
{
	int	aux;
	int	i;
	int	j;

	i = 0;
	j = -2147483648;
	while (i < size)
	{
		if (tab[i] == j)
		{
			aux = tab[j];
			tab[j] = tab[i];
			tab[i] = aux;
			j = -2147483648;
			i++;
		}
		else
		{
			j++;
		}
	}
}
